def drop_path(x, drop_prob):
    if drop_prob > 0.:
        keep_prob = 1. - drop_prob

        mask = torch.cuda.FloatTensor(x.size(0), 1, 1, 1).bernoulli_(keep_prob)
        x.div_(keep_prob)
        try:
            x.mul_(mask)
        except:
            mask = torch.cuda.HalfTensor(x.size(0), 1, 1, 1).bernoulli_(keep_prob)
            x.mul_(mask)
    return x


